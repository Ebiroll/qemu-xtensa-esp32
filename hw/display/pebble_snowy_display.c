// -----------------------------------------------------------------------------
static void ps_display_backlight_enable_cb(void *opaque, int n, int level)
{
    PSDisplayGlobals *s = (PSDisplayGlobals *)opaque;
    assert(n == 0);

    bool enable = (level != 0);
    if (s->backlight_enabled != enable) {
        s->backlight_enabled = enable;
        s->redraw = true;
    }
}


// -----------------------------------------------------------------------------
// Set brightness, from 0 to 255
static void ps_display_set_backlight_level_cb(void *opaque, int n, int level)
{
    PSDisplayGlobals *s = (PSDisplayGlobals *)opaque;
    assert(n == 0);

    float bright_f = (float)level / 255;

    // Temp hack - the Pebble sets the PWM to 25% for max brightness
    float new_setting = MIN(1.0, bright_f * 4);
    if (new_setting != s->brightness) {
        s->brightness = MIN(1.0, bright_f * 4);
        if (s->backlight_enabled) {
            s->redraw = true;
        }
    }
}

// -----------------------------------------------------------------------------
static void ps_display_vibe_ctl(void *opaque, int n, int level)
{
    PSDisplayGlobals *s = (PSDisplayGlobals *)opaque;
    assert(n == 0);

    s->vibrate_on = (level != 0);
    s->redraw = true;
}


// -----------------------------------------------------------------------------
static void ps_display_power_ctl(void *opaque, int n, int level)
{
    PSDisplayGlobals *s = (PSDisplayGlobals *)opaque;
    assert(n == 0);

    if (!level && s->power_on) {
        memset(s->framebuffer, 0, s->bytes_per_frame);
        ps_set_redraw(s);
        s->power_on = false;
    }
    s->power_on = !!level;
}


// -----------------------------------------------------------------------------
static void ps_display_reset(DeviceState *dev)
{
    PSDisplayGlobals *s = (PSDisplayGlobals *)dev;
    memset(s->framebuffer, 0, s->bytes_per_frame);
    ps_set_redraw(s);
}


// -----------------------------------------------------------------------------
static int ps_display_init(SSISlave *dev)
{
    PSDisplayGlobals *s = FROM_SSI_SLAVE(PSDisplayGlobals, dev);

    s->brightness = 0.0;
    s->backlight_enabled = false;
    s->cmd_set = PSDISPLAY_CMD_SET_0;
    ps_set_state(s, PSDISPLAYSTATE_ACCEPTING_CMD);

    // Allocate the frame buffer
    s->bytes_per_row = s->num_cols;
    s->bytes_per_frame = s->bytes_per_row * s->num_rows;
    s->framebuffer = g_malloc(s->num_rows * s->bytes_per_row);
    s->framebuffer_copy = g_malloc(s->num_rows * s->bytes_per_row);

    s->con = graphic_console_init(DEVICE(dev), 0, &ps_display_ops, s);
    qemu_console_resize(s->con, s->num_cols, s->num_rows);

    /* Create our inputs that will be connected to GPIOs from the STM32 */
    qdev_init_gpio_in_named(DEVICE(dev), ps_display_set_reset_pin_cb,
                            "reset", 1);

    /* Create our inputs that will be connected to GPIOs from the STM32 */
    qdev_init_gpio_in_named(DEVICE(dev), ps_display_set_sclk_pin_cb,
                            "sclk", 1);

    /* This callback informs us that brightness control is enabled */
    qdev_init_gpio_in_named(DEVICE(dev), ps_display_backlight_enable_cb,
                            "backlight_enable", 1);

    /* This callback informs us of the brightness level (from 0 to 255) */
    qdev_init_gpio_in_named(DEVICE(dev), ps_display_set_backlight_level_cb,
                            "backlight_level", 1);

    /* This callback informs us that the vibrate is on/orr */
    qdev_init_gpio_in_named(DEVICE(dev), ps_display_vibe_ctl,
                            "vibe_ctl", 1);

    /* This callback informs us that power is on/orr */
    qdev_init_gpio_in_named(DEVICE(dev), ps_display_power_ctl,
                            "power_ctl", 1);

    return 0;
}

